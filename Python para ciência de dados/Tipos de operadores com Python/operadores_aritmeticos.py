produto_1 = 40
produto_2 = 20

# Adição
resultado_1 = produto_1 + produto_2
print(resultado_1)

# Subtração
resultado_2 = produto_1 - produto_2
print(resultado_2)

# Multiplicação
resultado_3 = produto_1 * produto_2
print(resultado_3)

# Divisão
resultado_4 = produto_1 / produto_2 # Divisão float()
resultado_5 = produto_1 // produto_2 # Divisão inteira
print(resultado_4, resultado_5)

# Módulo
resultado_6 = produto_1 % produto_2
print(resultado_6)

# Exponenciação
resultado_7 = produto_1 ** produto_2
print(resultado_7)