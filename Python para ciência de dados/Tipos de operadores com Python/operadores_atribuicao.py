# Atribuição simples
salario = 1000
print(salario)

# Substituição de atribuição
salario = 1200
print(salario)

# Atrubuição com operadores aritméticos
salario += 100
print(salario)

salario -= 100
print(salario)

salario //= 10
print(salario)

salario /= 1
print(salario)

salario *= 10
print(salario)

salario %= 10
print(salario)

salario **= 10
print(salario)