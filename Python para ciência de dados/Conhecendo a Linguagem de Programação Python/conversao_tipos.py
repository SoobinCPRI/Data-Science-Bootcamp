# INT
valor = "10"
valor_int = int(valor)
print(type(valor_int))

# FLOAT
valor_1 = 10
valor_float = float(valor_1)
print(type(valor_float))

# STRING
valor_2 = 10.0
valor_str = str(valor_2)
print(type(valor_str))

print(5/2) # Divisão simples converte o resultado para float
print(5//3) # Divisão inteira as casa decimais são ignoradas e o resultado é sempre inteiro